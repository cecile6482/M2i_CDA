// Avec une boucle for 

console.log("Je commence à compter :");

for (let i = 1; i <= 10; i++) {
    console.log(i);
}

console.log("Fin du compte !");


// Avec une boucle while

let i = 1;

console.log("Je commence à compter :");

while (i <= 10) {
    console.log(i);
    i++;
}

console.log("Fin du compte !");

